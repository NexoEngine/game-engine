#include <glm/glm.hpp>
#include <gtest/gtest.h>

#include "Json.hpp"
#include "save/Serialization.hpp"
#include "save/glm/glm.hpp"

using json = nlohmann::json;

struct Transform {
    glm::vec3 position;
    glm::quat rotation;
    glm::vec3 scale;
};

TEST(SerializationIntegrationSmoke, TransformRoundTrip) {
    Transform original{};
    original.position = glm::vec3(1.0f, 2.0f, 3.0f);
    original.rotation = glm::quat(1.0f, 0.0f, 0.0f, 0.0f);
    original.scale = glm::vec3(1.0f);

    json j;
    nexo::save::serialize(j["position"], original.position);
    nexo::save::serialize(j["rotation"], original.rotation);
    nexo::save::serialize(j["scale"], original.scale);

    Transform restored{};
    nexo::save::deserialize(j["position"], restored.position);
    nexo::save::deserialize(j["rotation"], restored.rotation);
    nexo::save::deserialize(j["scale"], restored.scale);

    EXPECT_FLOAT_EQ(restored.position.x, original.position.x);
    EXPECT_FLOAT_EQ(restored.rotation.w, original.rotation.w);
    EXPECT_FLOAT_EQ(restored.scale.z, original.scale.z);
}
