#pragma once

namespace ImNexo {

    inline bool g_isItemActive      = false;
    inline bool g_isItemActivated   = false;
    inline bool g_isItemDeactivated = false;

    /**
     * @brief Check if the last item is currently active.
     *
     * This function returns true if the last item is currently active, meaning it is being interacted with.
     * An item is considered active when it is clicked or focused.
     *
     * @return true if the last item is active, false otherwise.
     */
    bool isItemActive();

    /**
     * @brief Mark the last item as active.
     *
     * This function sets the internal state to indicate that the last item is currently active.
     * An item is considered active when it is being interacted with, such as when it is clicked
     * or focused.
     */
    void setItemActive();

    /**
     * @brief Check if the last item was activated (e.g., clicked or focused).
     *
     * This function returns true if the last item was activated during the current frame.
     * Activation typically occurs when an item is clicked or receives keyboard focus.
     *
     * @return true if the last item was activated, false otherwise.
     */
    bool isItemActivated();
    /**
     * @brief Mark the last item as activated.
     *
     * This function sets the internal state to indicate that the last item was activated
     * during the current frame. Activation typically occurs when an item is clicked or
     * receives keyboard focus.
     */
    void setItemActivated();

    /**
     * @brief Check if the last item was deactivated (e.g., lost focus or interaction).
     *
     * This function returns true if the last item was deactivated during the current frame.
     * Deactivation typically occurs when an item loses focus or interaction.
     *
     * @return true if the last item was deactivated, false otherwise.
     */
    bool isItemDeactivated();

    /**
     * @brief Mark the last item as deactivated.
     *
     * This function sets the internal state to indicate that the last item was deactivated
     * during the current frame. Deactivation typically occurs when an item loses focus or
     * interaction.
     */
    void setItemDeactivated();

    /**
     * @brief Reset all item states (active, activated, deactivated).
     *
     * This function resets the internal states for active, activated, and deactivated
     * items. It should be called at the end of each frame to clear all item states
     * for the next frame.
     */
    void resetItemStates();

} // namespace ImNexo
