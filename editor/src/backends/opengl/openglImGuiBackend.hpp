#pragma once

#include <GLFW/glfw3.h>

namespace nexo::editor {
	/**
     * @brief Implements the ImGui backend for OpenGL using GLFW.
     *
     * This class wraps the initialization, shutdown, frame handling,
     * and error callback functionality for using ImGui with OpenGL and GLFW.
     */
    class OpenGLImGuiBackend {
        public:
			/**
			* @brief Initializes the ImGui backend for OpenGL.
			*
			* Sets up both the GLFW and OpenGL3 parts of the backend.
			*
			* @param window Pointer to the GLFW window.
			* @throws BackendRendererApiInitFailed if initialization fails.
			*/
            static void init(GLFWwindow *window);

            /**
             * @brief Shuts down the ImGui OpenGL backend.
             *
             * Cleans up the OpenGL3 and GLFW bindings and destroys the ImGui context.
             */
            static void shutdown();

            /**
             * @brief Initializes the ImGui font atlas.
             *
             * This function creates the fonts texture for ImGui.
             *
             * @throws BackendRendererApiFontInitFailed if font atlas initialization fails.
             */
            static void initFontAtlas();

            /**
             * @brief Begins a new ImGui frame.
             *
             * Polls GLFW events, starts new frames for both the OpenGL3 and GLFW backends,
             * and then begins a new ImGui frame.
             */
            static void begin();

            /**
             * @brief Ends the current ImGui frame and renders the draw data.
             *
             * Renders the ImGui draw data using the OpenGL3 backend and swaps the GLFW window buffers.
             *
             * @param window Pointer to the GLFW window.
             */
            static void end(GLFWwindow *window);

            /**
             * @brief Returns the current OpenGL error callback.
             *
             * The returned callback can be used to handle OpenGL-related errors.
             *
             * @return A pointer to the error callback function.
             */
            static void *getErrorCallback();
    };
}
