#pragma once

#include <string>

#include "inputs/WindowState.hpp"

namespace nexo::editor {

    using WindowId = unsigned int;

    inline WindowId nextWindowId = 0;
    class IDocumentWindow {
       public:
        virtual ~IDocumentWindow() = default;

        /**
         * @brief Initializes the window, setting up necessary resources and state.
         */
        virtual void setup() = 0;

        /**
         * @brief Shuts down the window, releasing resources and cleaning up state.
         */
        virtual void shutdown() = 0;

        /**
         * @brief Renders the window's content.
         *
         * This method is responsible for drawing the window's UI elements and handling user interactions.
         */
        virtual void show() = 0;

        /**
         * @brief Updates the window's state.
         *
         * This method is called to refresh the window's content and handle any necessary updates.
         */
        virtual void update() = 0;

        /**
         * @brief Checks if the window is currently focused.
         * @return true if the window is focused, false otherwise.
         */
        [[nodiscard]] virtual bool isFocused() const = 0;
        /**
         * @brief Checks if the window is currently opened.
         * @return true if the window is opened, false otherwise.
         */
        [[nodiscard]] virtual bool isOpened() const = 0;

        /**
         * @brief Sets the opened state of the window.
         * @param opened true to open the window, false to close it.
         */
        virtual void setOpened(bool opened) = 0;

        /**
         * @brief Checks if the window is currently hovered by the mouse.
         * @return true if the window is hovered, false otherwise.
         */
        [[nodiscard]] virtual bool isHovered() const = 0;

        /**
         * @brief Gets the size of the window's content area.
         * @return Reference to the ImVec2 representing the content size.
         */
        [[nodiscard]] virtual const ImVec2 &getContentSize() const = 0;

        /**
         * @brief Gets a reference to the opened state of the window.
         * @return Reference to the boolean indicating if the window is opened.
         */
        [[nodiscard]] virtual bool &getOpened() = 0;

        /**
         * @brief Gets the name of the window.
         * @return Reference to the string containing the window's name.
         */
        [[nodiscard]] virtual const std::string &getWindowName() const = 0;

        /**
         * @brief Gets the current state of the window.
         * @return Reference to the WindowState object representing the window's state.
         */
        [[nodiscard]] virtual const WindowState &getWindowState() const = 0;
    };
} // namespace nexo::editor
