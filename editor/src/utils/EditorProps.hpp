#pragma once

#include "ecs/Coordinator.hpp"

namespace nexo::editor::utils {

    /**
     * @enum PropsType
     * @brief Enumeration representing different types of editor props that can be added to entities.
     *
     * This enum defines the types of props that can be associated with entities in the editor.
     * Each type corresponds to a specific visual representation or functionality within the editor environment.
     */
    enum class PropsType { CAMERA, POINT_LIGHT, SPOT_LIGHT };

  /**
     * @brief Adds editor props to a given entity based on the specified PropsType.
     *
     * This function attaches visual and functional components to the provided entity
     * according to the type of props specified. It supports adding camera props,
     * point light props, and spotlight props, each represented by distinct textures
     * and materials.
     *
     * @param entity The ECS entity to which the props will be added.
     * @param type The type of props to add, defined by the PropsType enum.
     */
    void addPropsTo(ecs::Entity entity, PropsType type);
} // namespace nexo::editor::utils
