#pragma once

#include "AEntityProperty.hpp"

namespace nexo::editor {
    class CameraTarget final : public AEntityProperty {
       public:
        using AEntityProperty::AEntityProperty;

        /**
         * @brief Renders the camera target properties UI for the specified entity.
         *
         * Retrieves the PerspectiveCameraTarget component associated with the entity and displays
         * a header along with a 3D vector editor widget. When the header is open, the widget
         * allows the user to modify the camera target position, which is then applied to the component.
         *
         * @param entity The ECS entity whose camera target properties are to be displayed.
         */
        void show(ecs::Entity entity) override;
    };
} // namespace nexo::editor
