#pragma once

#include <cstdint>

namespace nexo::renderer {

    struct RequiredAttributes {
        union BitFields {
            struct Flags {
                bool position : 1;
                bool normal : 1;
                bool tangent : 1;
                bool bitangent : 1;
                bool uv0 : 1;
                bool lightmapUV : 1;
                bool padding : 2;
            } flags;
            uint8_t bits = 0;
        } bitsUnion;

        /**
         * @brief Equality operator to compare two RequiredAttributes instances.
         *
         * This operator checks if two RequiredAttributes instances have the same set of required attributes.
         *
         * @param o The other RequiredAttributes instance to compare against.
         * @return true if both instances have the same required attributes, false otherwise.
         *
         * Example:
         * ```cpp
         * RequiredAttributes req1{.bitsUnion = {.flags = {true, false, true, false, true, false, false, false}}};
         * RequiredAttributes req2{.bitsUnion = {.flags = {true, false, true, false, true, false, false, false}}};
         * bool areEqual = (req1 == req2); //returns true
         * ```
         */
        bool operator==(RequiredAttributes const& o) const
        {
            return bitsUnion.bits == o.bitsUnion.bits;
        }

        /**
         * @brief Checks if the current RequiredAttributes is compatible with another.
         *
         * Compatibility means that all attributes required by the current instance
         * are also present in the other instance.
         *
         * @param o The other RequiredAttributes to compare against.
         * @return true if compatible, false otherwise.
         *
         * Example:
         * ```cpp
         * RequiredAttributes req1{.bitsUnion = {.flags = {true, false, true, false, true, false, false, false}}};
         * RequiredAttributes req2{.bitsUnion = {.flags = {true, true, true, false, true, true, false, false}}};
         * bool isCompatible = req1.compatibleWith(req2); //returns true
         * ```
         */
        [[nodiscard]] bool compatibleWith(RequiredAttributes const& o) const
        {
            return (bitsUnion.bits & o.bitsUnion.bits) == bitsUnion.bits;
        }
    };

} // namespace nexo::renderer
