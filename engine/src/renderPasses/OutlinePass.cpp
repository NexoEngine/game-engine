#include "OutlinePass.hpp"
#include "DrawCommand.hpp"
#include "Masks.hpp"
#include "Passes.hpp"
#include "RenderCommand.hpp"
#include "renderer/RenderPipeline.hpp"

#include <glad/glad.h>

namespace nexo::renderer {
    OutlinePass::OutlinePass() : RenderPass(Passes::OUTLINE, "Outline pass")
    {}

    void OutlinePass::execute(RenderPipeline& pipeline)
    {
        const auto &globalUniforms = pipeline.getGlobalUniforms();

        const auto renderTarget = pipeline.getRenderTarget();
        auto maskFb = getPrerequisiteOutput(pipeline, Passes::MASK);
        if (!renderTarget || !maskFb) return;

        renderTarget->bind();
        constexpr GLenum ourDrawBuffers[] = {GL_COLOR_ATTACHMENT0};
        glDrawBuffers(1, ourDrawBuffers);

        renderer::NxRenderCommand::setDepthTest(false);
        renderer::NxRenderCommand::setDepthMask(false);
        maskFb->bindAsTexture(0,0);           // bound to unit 0
        renderTarget->bindDepthAsTexture(1); // bound to unit 1
        maskFb->bindDepthAsTexture(2);     // bound to unit 2

        auto& drawCommands = pipeline.getDrawCommands();
        for (auto &cmd : drawCommands) {
            if (cmd.filterMask & F_OUTLINE_PASS) {
                cmd.execute(&globalUniforms);
            }
        }

        // Restore all color attachment draw buffers from the render target
        const unsigned int nbAttachments = renderTarget->getNbColorAttachments();
        std::vector<GLenum> restoreBuffers(nbAttachments);
        for (unsigned int i = 0; i < nbAttachments; ++i)
            restoreBuffers[i] = GL_COLOR_ATTACHMENT0 + i;
        glDrawBuffers(static_cast<GLsizei>(nbAttachments), restoreBuffers.data());
        renderer::NxRenderCommand::setDepthMask(true);
        renderer::NxRenderCommand::setDepthTest(true);

        renderTarget->unbind();
    }
} // namespace nexo::renderer
