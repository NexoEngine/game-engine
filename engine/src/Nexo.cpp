#include "Nexo.hpp"

namespace nexo {

    Application &init()
    {
        Application &app = Application::getInstance();
        app.init();
        LOG(NEXO_INFO, "Nexo engine initialized");
        return app;
    }

    Application &getApp()
    {
        return Application::getInstance();
    }

    void runEngine(const Application::SceneInfo &sceneInfo)
    {
        Application &app = Application::getInstance();
        app.run(sceneInfo);
    }

}
