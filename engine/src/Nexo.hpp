namespace nexo {
    /**
     * @brief Sets the application instance to a derived class of Application.
     *
     * This function allows you to specify a custom application class that inherits from
     * the base Application class. It forwards any constructor arguments to the derived
     * class's constructor and sets it as the singleton instance of the Application.
     *
     * @tparam DerivedApp The type of the derived application class.
     * @tparam Args The types of the constructor arguments for the derived class.
     * @param args The constructor arguments to be forwarded to the derived class.
     */
    template<typename DerivedApp, typename... Args>
    void useApp(Args &&...args)
    {
        Application::setInstance<DerivedApp, Args...>(std::forward<Args>(args)...);
    }

    /**
     * @brief Initializes the Nexo engine and returns the main application instance.
     *
     * This function initializes the singleton instance of the Application class,
     * setting up necessary components and systems for the engine to function.
     * It logs a message indicating successful initialization.
     *
     * @return Reference to the initialized Application instance.
     */
    Application &init();

    /**
     * @brief Retrieves the singleton instance of the Application class.
     *
     * This function provides access to the main application instance, allowing
     * other parts of the code to interact with the application state and functionality.
     *
     * @return Reference to the singleton Application instance.
     */
    Application &getApp();

    /**
     * @brief Runs the main application loop with the specified scene.
     *
     * This function retrieves the singleton instance of the Application class
     * and invokes its run method, passing in the provided SceneInfo object.
     *
     * @param sceneInfo Information about the scene to be rendered and updated.
     */
    void runEngine(const Application::SceneInfo &sceneInfo);
} // namespace nexo
