#include <ranges>
#include <algorithm>
#include <utf8cpp/utf8.h>

#include "HostString.hpp"

namespace nexo::scripting {

    void HostString::init_from_utf8(const std::string& utf8)
    {
        #ifdef _WIN32
            std::wstring utf16;
            utf8::utf8to16(utf8.begin(), utf8.end(), std::back_inserter(utf16));
            m_buffer.resize(utf16.size() + 1);
            std::ranges::copy(utf16, m_buffer.data());
            m_buffer.back() = L'\0';
        #else
            m_buffer.resize(utf8.size() + 1);
            std::ranges::copy(utf8, m_buffer.data());
            m_buffer.back() = '\0';
        #endif
    }

    void HostString::init_from_wide(const std::wstring& wide)
    {
        #ifdef _WIN32
            m_buffer.resize(wide.size() + 1);
            std::ranges::copy(wide, m_buffer.data());
            m_buffer.back() = L'\0';
        #else
            std::string utf8;
            utf8::utf16to8(wide.begin(), wide.end(), std::back_inserter(utf8));
            m_buffer.resize(utf8.size() + 1);
            std::ranges::copy(utf8, m_buffer.data());
            m_buffer.back() = '\0';
        #endif
    }

    std::string HostString::to_utf8() const
    {
        #ifdef _WIN32
            std::string utf8;
            utf8::utf16to8(m_buffer.data(), m_buffer.data() + size(), std::back_inserter(utf8));
            return utf8;
        #else
            return {m_buffer.data(), size()};
        #endif
    }

    std::wstring HostString::to_wide() const
    {
        #ifdef _WIN32
            return std::wstring(m_buffer.data(), size());
        #else
            std::wstring utf16;
            utf8::utf8to16(m_buffer.data(), m_buffer.data() + size(), std::back_inserter(utf16));
            return utf16;
        #endif
    }

} // namespace nexo::scripting
