#pragma once

#include "components/Light.hpp"
#include "components/RenderContext.hpp"
#include "components/SceneComponents.hpp"
#include "ecs/GroupSystem.hpp"

namespace nexo::system {

    /**
     * @brief System responsible for updating directional lights in the scene.
     *
     * This system iterates over all directional light entities in the active scene and updates
     * the RenderContext's sceneLights collection with their directional light components.
     *
     * @note Component Access Rights:
     *  - READ access to components::DirectionalLightComponent (owned)
     *  - READ access to components::SceneTag (non-owned)
     *  - WRITE access to components::RenderContext (singleton)
     *
     * @note The system uses scene partitioning to only process directional light entities
     * belonging to the currently active scene (identified by RenderContext.sceneRendered).
     *
     * @throws TooManyDirectionalLightsException if the count of directional light entities exceeds
     * MAX_DIRECTIONAL_LIGHTS.
     */
    class DirectionalLightsSystem final
        : public ecs::GroupSystem<ecs::Owned<ecs::Read<components::DirectionalLightComponent>>,
                                  ecs::NonOwned<ecs::Read<components::SceneTag>>,
                                  ecs::WriteSingleton<components::RenderContext>> {
       public:
        /**
         * @brief Updates the RenderContext with directional lights from the active scene.
         *
         * This method retrieves the currently active scene from the RenderContext singleton.
         * It then partitions entities by their SceneTag to find those belonging to the active scene.
         * If directional light entities are found, their components are collected and used to update
         * the RenderContext's sceneLights collection. If the number of directional lights exceeds
         * MAX_DIRECTIONAL_LIGHTS, a TooManyDirectionalLightsException is thrown.
         * If no directional lights are found for the active scene, a warning is logged.
         */
        void update();
    };
} // namespace nexo::system
