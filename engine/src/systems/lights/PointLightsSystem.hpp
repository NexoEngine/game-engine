#pragma once

#include "components/Light.hpp"
#include "components/RenderContext.hpp"
#include "components/SceneComponents.hpp"
#include "ecs/GroupSystem.hpp"

namespace nexo::system {

    /**
     * @brief System responsible for updating point lights in the scene.
     *
     * This system iterates over all point light entities in the active scene and updates
     * the RenderContext's sceneLights collection with their point light components.
     *
     * @note Component Access Rights:
     *  - READ access to components::PointLightComponent (owned)
     *  - READ access to components::SceneTag (non-owned)
     *  - WRITE access to components::RenderContext (singleton)
     *
     * @note The system uses scene partitioning to only process point light entities
     * belonging to the currently active scene (identified by RenderContext.sceneRendered).
     *
     * @throws TooManyPointLightsException if the count of point light entities exceeds MAX_POINT_LIGHTS.
     */
    class PointLightsSystem final : public ecs::GroupSystem<ecs::Owned<ecs::Read<components::PointLightComponent>>,
                                                            ecs::NonOwned<ecs::Read<components::SceneTag>>,
                                                            ecs::WriteSingleton<components::RenderContext>> {
       public:
        /**
         * @brief Updates the RenderContext with point lights from the active scene.
         *
         * This method retrieves the currently active scene from the RenderContext singleton.
         * It then partitions entities by their SceneTag to find those belonging to the active scene.
         * If point light entities are found, their components are collected and used to update
         * the RenderContext's sceneLights collection. If the number of point lights exceeds
         * MAX_POINT_LIGHTS, a TooManyPointLightsException is thrown.
         * If no point lights are found for the active scene, a warning is logged.
         */
        void update();
    };
} // namespace nexo::system
