#pragma once

#include "components/RenderContext.hpp"
#include "components/SceneComponents.hpp"
#include "components/Transform.hpp"
#include "ecs/QuerySystem.hpp"

namespace nexo::system {
    class TransformMatrixSystem final
        : public ecs::QuerySystem<ecs::Write<components::TransformComponent>, ecs::Read<components::SceneTag>,
                                  ecs::WriteSingleton<components::RenderContext>> {
       public:
        /**
         * @brief Updates the world matrices of entities based on their TransformComponents.
         * This method iterates over all entities in the system, checking if their SceneTag matches
         * the currently active scene from the RenderContext singleton. For matching entities,
         * it computes the local transformation matrix from the TransformComponent's position,
         * rotation (quaternion), and scale, and sets both the localMatrix and worldMatrix
         * of the TransformComponent accordingly.
         */
        void update();

       private:
        /** @brief Creates a transformation matrix from a TransformComponent.
         * This static helper function constructs a transformation matrix by combining translation,
         * rotation (from quaternion), and scaling transformations in that order.
         * @param transform The TransformComponent containing position, rotation (as quaternion), and scale.
         * @return glm::mat4 The resulting transformation matrix.
         */
        static glm::mat4 createTransformMatrix(const components::TransformComponent &transform);
    };
} // namespace nexo::system
