#pragma once

#include <string>

#include "scripting/native/systems/ManagedWorldState.hpp"

namespace nexo::system {

    class ScriptingSystem {
       public:
        ScriptingSystem();
        ~ScriptingSystem() = default;

        /**
         * @brief Initializes the scripting system by setting up the world state and initializing components and
         * systems.
         *
         * This method retrieves the singleton instance of the scripting host handler, updates the world state,
         * and initializes the scripting components and systems. If any step fails, it logs an error message
         * and returns a non-zero error code.
         *
         * @return int Returns 0 on success, or a non-zero error code on failure.
         */
        int init();

        /** @brief Updates the scripting system by refreshing the world state and invoking the update method on managed
         * systems.
         *
         * This method first updates the internal world state to reflect any changes in the game environment.
         * It then calls the Update method on the managed systems, passing in the updated world state.
         * If the update fails, it logs an error message and returns a non-zero error code.
         *
         * @return int Returns 0 on success, or a non-zero error code on failure.
         */
        int update();

        /**
         * @brief Shuts down the scripting system by invoking the shutdown method on managed systems and terminating the
         * host.
         *
         * This method calls the Shutdown method on the managed systems to perform any necessary cleanup.
         * It then terminates the scripting host handler instance. If any step fails, it logs an error message
         * and returns a non-zero error code.
         *
         * @return int Returns 0 on success, or a non-zero error code on failure.
         */
        int shutdown();

       private:
        /**
         * @brief Updates the internal world state used by the scripting system.
         *
         * This method refreshes the m_worldState member variable to ensure it accurately reflects
         * the current state of the game world. This may involve gathering data from various game
         * systems and components to populate the ManagedWorldState structure.
         */
        void updateWorldState();

        std::string m_latestScriptingError;
        scripting::ManagedWorldState m_worldState = {};
    };

} // namespace nexo::system
