#include "WindowEvent.hpp"

namespace nexo::event {

    std::ostream& operator<<(std::ostream& os, const KeyAction& action)
    {
        using enum KeyAction;

        switch (action) {
            case PRESSED:
                os << "PRESSED";
                break;
            case RELEASED:
                os << "RELEASED";
                break;
            case REPEAT:
                os << "REPEAT";
                break;
            default:
                os << "UNKNOWN_ACTION";
                break;
        }
        return os;
    }

    std::ostream& operator<<(std::ostream& os, const KeyMods& mod)
    {
        using enum KeyMods;

        switch (mod) {
            case NONE:
                os << "NONE";
                break;
            case SHIFT:
                os << "SHIFT";
                break;
            case CONTROL:
                os << "CONTROL";
                break;
            case ALT:
                os << "ALT";
                break;
            default:
                os << "UNKNOWN_MOD";
                break;
        }
        return os;
    }

    std::ostream& operator<<(std::ostream& os, const MouseButton& button)
    {
        using enum MouseButton;

        switch (button) {
            case LEFT:
                os << "LEFT";
                break;
            case RIGHT:
                os << "RIGHT";
                break;
            case MIDDLE:
                os << "MIDDLE";
                break;
            default:
                os << "UNKNOWN_MOD";
                break;
        }
        return os;
    }

} // namespace nexo::event
